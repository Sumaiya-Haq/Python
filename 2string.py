# Assigning a string using double quotes
name = "Sumaiya haq"

# Assigning the same string using single quotes
name1 = 'Sumaiya haq'

# Assigning the same string using triple quotes (also used for multi-line strings)
name2 = '''Sumaiya haq'''

# Printing the value stored in variable 'name'
print(name)

# Printing the value stored in variable 'name1'
print(name1)

# Printing the value stored in variable 'name2'
print(name2)

# Assigning an integer value to variable 'age'
age = 22


